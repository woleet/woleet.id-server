export function serializeServerConfig(config: InternalServerConfigObject): ApiServerConfig {
  const {
    fallbackOnDefaultKey,
    defaultKeyId,
    identityURL,
    APIURL,
    logoURL,
    HTMLFrame,
    allowUserToSign,
    enableOpenIDConnect,
    openIDConnectURL,
    openIDConnectClientId,
    openIDConnectClientSecret,
    openIDConnectClientRedirectURL,
    enableOIDCP,
    OIDCPInterfaceURL,
    OIDCPProviderURL,
    OIDCPIssuerURL,
    OIDCPClients,
    keyExpirationOffset,
    enrollmentExpirationOffset,
    enableSMTP,
    SMTPConfig,
    webClientURL,
    mailResetPasswordTemplate,
    mailOnboardingTemplate,
    mailKeyEnrollmentTemplate,
    contact,
    organizationName,
    proofDeskAPIURL,
    proofDeskAPIToken,
    enableProofDesk,
    blockPasswordInput,
    askForResetInput,
  } = config;

  return {
    fallbackOnDefaultKey,
    defaultKeyId,
    identityURL,
    APIURL,
    logoURL,
    HTMLFrame,
    allowUserToSign,
    enableOpenIDConnect,
    openIDConnectURL,
    openIDConnectClientId,
    openIDConnectClientSecret,
    openIDConnectClientRedirectURL,
    enableOIDCP,
    OIDCPInterfaceURL,
    OIDCPProviderURL,
    OIDCPIssuerURL,
    OIDCPClients,
    keyExpirationOffset,
    enrollmentExpirationOffset,
    enableSMTP: enableSMTP,
    SMTPConfig,
    webClientURL,
    mailResetPasswordTemplate,
    mailOnboardingTemplate,
    mailKeyEnrollmentTemplate,
    contact,
    organizationName,
    proofDeskAPIURL,
    proofDeskAPIToken,
    enableProofDesk,
    blockPasswordInput,
    askForResetInput,
  };
}
